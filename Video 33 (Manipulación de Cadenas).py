"""
USO DE STRING EN PYTHON
upper()             <- conversión a mayusculas
lower()             <- conversión a minusculas
capitalize()        <- primera letra en mayuscula
count()             <- cuentas cuantas veces aparece un caracter o cadena en un texto
find()              <- representar indice (posición) en donde aparece un caracter/caracteres dentro de un texto
isdigit()           <- devuelve boolean (si es número o no)
isalum()            <- comprueba si es alfanumérico
isalpha()           <- comprueba si son solo letras
split()             <- separa por palabras utilizando espacios
strip()             <- borrar espacios sobrantes al principio o final
replace()           <- busca letra u oración y la reemplaza en una frase
rfind()             <- igual que find pero desde atrás hacia el inicio
"""

print(input("INGRESA TU NOMBRE: ").upper())
